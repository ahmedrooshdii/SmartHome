#define SET_BIT(REG,BIT_NO) (REG |= (1<<BIT_NO))               // set bit as (1)
#define CLEAR_BIT(REG,BIT_NO) (REG &= ~(1<<BIT_NO))            // clear bit (0)
#define CHECK_BIT(REG,BIT_NO) (REG & (1<<BIT_NO))             // check if bit is (1) or (0) and return (1) if bit is one and (0) if bit is zero
#define FLIP_BIT(REG,BIT_NO) (REG ^= (1<<BIT_NO))              // Flip bit to (0) if it was (1) and (0) if it was (1)

